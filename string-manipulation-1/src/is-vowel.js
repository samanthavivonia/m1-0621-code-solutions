function isVowel(character) {
  return (
    character === 'A' ||
    character === 'a' ||
    character === 'E' ||
    character === 'e' ||
    character === 'I' ||
    character === 'i' ||
    character === 'O' ||
    character === 'o' ||
    character === 'U' ||
    character === 'u');
}
